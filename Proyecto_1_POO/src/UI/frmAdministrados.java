    private void btnAgregarLicenciaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarLicenciaActionPerformed
        if (ValidarLicencia()) {
            SimpleDateFormat traductor = new SimpleDateFormat("dd-MM-yyyy");
            Date fechatemp;
            try {
                Licencia nuevaLicencia = new Licencia();
                nuevaLicencia.setNumero(Integer.parseInt(jtfLicenciaNombre.getText()));
                
                fechatemp = traductor.parse(jtfLicenciaFechaEmicion.getText());
                nuevaLicencia.setFechaEmision(jtfLicenciaFechaEmicion.getText());
                
                fechatemp = traductor.parse(jtfLicenciaFechaExpiracion.getText());
                nuevaLicencia.setFechaExpiracion(jtfLicenciaFechaExpiracion.getText());
                
                nuevaLicencia.setTipo(jtfLicenciaTipo.getText());
                
                licenciasChofer.add(nuevaLicencia);
                CargadorComboChofer();
                LimpiarLicencia();
                CargadorAprobar();
                
            } catch (ParseException pe) {
                JOptionPane.showMessageDialog(null, "Algun dato no tirnr \n"
                        + "el formato deseado",
                        "Alerta", 2);
            }
        } else {
            JOptionPane.showMessageDialog(null, "Falta Algun dato de la licencia",
                    "Alerta", 2);
        }
        

    }//GEN-LAST:event_btnAgregarLicenciaActionPerformed

