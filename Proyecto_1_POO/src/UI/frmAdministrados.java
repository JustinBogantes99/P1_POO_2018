    private void btnAgregarVehiculoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarVehiculoActionPerformed
        if (ValidarVehiculo()) {
            try {
                Vehiculo nuevoVehiculo = new Vehiculo();
                nuevoVehiculo.setPlaca(Integer.parseInt(jtfVehiculoPlaca.getText()));
                nuevoVehiculo.setAnioFabricación(Integer.parseInt(jtfVehiculoAnioFabricacion.getText()));
                nuevoVehiculo.setColor(jtfVehiculoColor.getText());
                nuevoVehiculo.setMarca(jtfVehiculoMarca.getText());
                nuevoVehiculo.setKilometraje(Double.parseDouble(jtfVehiculoKilometraje.getText()));
                nuevoVehiculo.setNúmeroVin(Integer.parseInt(jtfVehiculoVIN.getText()));
                nuevoVehiculo.setSedePertenenciaVehículo(jtfVehiculoSede.getText());
                
                if (usuario.RegistrarVehiculoNuevo(nuevoVehiculo)) {
                    JOptionPane.showMessageDialog(null, "El Vehiculo fue agregado\n"
                            + "exitosamente",
                            "Mensaje", 1);
                    LimpiarVehiculo();
                    CargadorAprobar();
                } else {
                    JOptionPane.showMessageDialog(null, "El Vehiculo no pudo\n"
                            + "Ser agregado",
                            "Alerta", 0);
                }
            } catch (NumberFormatException nfe) {
                JOptionPane.showMessageDialog(null, "Placa/Año/Kilometraje/VIN\n"
                        + "solo pueden ser numeros",
                        "Alerta", 2);
            }
        } else {
            JOptionPane.showMessageDialog(null, "Falta Algun dato por rellenar",
                    "Alerta", 2);
        }
        

    }//GEN-LAST:event_btnAgregarVehiculoActionPerformed

