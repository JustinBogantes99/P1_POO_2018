    /*----------Metodos de validacion de datos----------*/
    public boolean ValidarVehiculo() {
        if (jtfVehiculoPlaca.getText().equals("")
                || jtfVehiculoAnioFabricacion.getText().equals("")
                || jtfVehiculoColor.getText().equals("")
                || jtfVehiculoMarca.getText().equals("")
                || jtfVehiculoCapacidad.getText().equals("")
                || jtfVehiculoKilometraje.getText().equals("")
                || jtfVehiculoVIN.getText().equals("")
                || jtfVehiculoSede.getText().equals("")) {
            return false;
        }
        return true;
    }
    
    public boolean ValidarLicencia() {
        if (jtfLicenciaNombre.getText().equals("")
                || jtfLicenciaFechaExpiracion.getText().equals("")
                || jtfLicenciaFechaEmicion.getText().equals("")
                || jtfLicenciaTipo.getText().equals("")) {
            return false;
        }
        return true;
    }

    public boolean ValidarChofer() {
        if (jtfChoferNombre.getText().equals("")
                || jtfChoferCedula.getText().equals("")
                || jtfChoferCorreo.getText().equals("")
                || jtfChoferTelefono.getText().equals("")
                || licenciasChofer.isEmpty()) {
            return false;
        }
        return true;
    }
    public boolean ValidarSecretaria() {
        if (jtfSectretariaNombre.getText().equals("")
                || jtfSectretariaCorreo.getText().equals("")
                || jtfSectretariaDepartamento.getText().equals("")
                || jtfSectretariaTelefono.getText().equals("")) {
            return false;
        }
        return true;
    }

    /*----------Metodos de carga----------*/
    public void CargadorVentana(Administrador entrada) {
        usuario = entrada;
        this.setTitle("Administrador");
        
        ImageIcon imagen = new ImageIcon("src/Multimedia/FondoSecretaria.png");
        Icon icono = new ImageIcon(imagen.getImage().getScaledInstance(900,
                400, Image.SCALE_DEFAULT));
        lblFondo7.setIcon(icono);
        lblFondo2.setIcon(icono);
        lblFondo3.setIcon(icono);
        lblFondo1.setIcon(icono);
        lblFondo4.setIcon(icono);
        lblFondo5.setIcon(icono);
        lblFondo6.setIcon(icono);
        
        CargadorComboChofer();
        CargadorAprobar();
        
    }
    
    public void CargadorComboChofer() {
        jcbChoferLicencias.removeAllItems();
        lblCantLicencias.setText("Cant Licencias: " + licenciasChofer.size());
        for (Licencia temp : licenciasChofer) {
            jcbChoferLicencias.addItem(temp.getTipo());
        }
        
    }
    
    public void CargadorAprobar() {
        jcbAprobarChofer.removeAllItems();
        jcbAprobarVehiculo.removeAllItems();
        
        for (Chofer temp : main.miControlador.getMiTec().getChoferes()) {
            jcbAprobarChofer.addItem(temp.getNombreCompleto());
        }
        
        for (Vehiculo temp : main.miControlador.getMiTec().getVehiculos()) {
            jcbAprobarVehiculo.addItem("Placa: "+temp.getPlaca());
        }
        
    }

