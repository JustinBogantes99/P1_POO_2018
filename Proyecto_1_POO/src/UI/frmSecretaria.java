    public void CargadorVentana(Secretaria entrada) {
        this.setTitle("Loggin");

        ImageIcon imagen = new ImageIcon("src/Multimedia/FondoSecretaria.png");
        Icon icono = new ImageIcon(imagen.getImage().getScaledInstance(700,
                400, Image.SCALE_DEFAULT));
        lblFondoRegistrar.setIcon(icono);

        usuarioActual = entrada;
        CargadorPasajeros();
        CargadorSolicitarPasajerosAgregados();
    }

    public void CargadorPasajeros() {
        List<Pasajero> temp = implementacion.miControlador.ObtenerListaUsuarios();
        jcbPasajeros.removeAllItems();
        for (Pasajero auxiliar : temp) {
            jcbPasajeros.addItem(auxiliar.getNombreCompleto());
        }

    }

    public void CargadorSolicitarPasajerosAgregados() {
        jcbSolicitarQuitarPasajeros.removeAllItems();
        for (Pasajero auxiliar : listaPasajerosViaje) {
            jcbSolicitarQuitarPasajeros.addItem(auxiliar.getNombreCompleto());
        }
        lblCantPasajeros.setText("Cant. Pasajeros: " + listaPasajerosViaje.size());
    }

    public boolean ValidadorSolicitarPasajero(Pasajero temporal) {
        for (Pasajero Auxiliar : listaPasajerosViaje) {
            if (Auxiliar.getCedula() == temporal.getCedula()) {
                JOptionPane.showMessageDialog(null, "No se puede Agregar\n"
                        + "al pasajero: " + temporal.getNombreCompleto()
                        + "\nCedula:" + temporal.getCedula()
                        + "\nYa que esta Agregado", "Alerta", 0);
                return false;
            }
        }
        return true;
    }

