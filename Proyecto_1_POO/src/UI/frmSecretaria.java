    private void btnRegistrarPasajeroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarPasajeroActionPerformed
        try {
            if (ValidarCamposAgregarUsuario()) {
                Pasajero nuevoPasajero = new Pasajero();
                nuevoPasajero.setNombreCompleto(jtfRegistrarNombre.getText());
                nuevoPasajero.setCedula(Integer.parseInt(jtfRegistrarCedula.getText()));
                nuevoPasajero.setDireccion(jtfRegistrarDireccion.getText());
                nuevoPasajero.setCorreoElectronico(jtfRegistrarCorreo.getText());
                nuevoPasajero.setTelefono(Integer.parseInt(jtfRegistrarNombre.getText()));

                if (usuarioActual.RegistrarPasajero(nuevoPasajero)) {
                    JOptionPane.showMessageDialog(null, "Pasajero Registrado Exitosamente",
                            "Mensaje", 1);
                    CargadorPasajeros();
                    LimpiarCamposAgregarUsuario();
                    //Borrar Campos
                } else {
                    JOptionPane.showMessageDialog(null, "Pasajero No pudo ser Registrado",
                            "Alerta", 0);
                    //Borrar Campos
                }
            }

        } catch (NumberFormatException nfe) {
            JOptionPane.showMessageDialog(null, "El campo de cedula o Telefono\n"
                    + "solo pueden tener numeros", "Alerta", 2);
        }


    }//GEN-LAST:event_btnRegistrarPasajeroActionPerformed

    private void btnSolicitarAgregarPasajeroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSolicitarAgregarPasajeroActionPerformed
        Pasajero temporal = implementacion.miControlador.getMiTec().
                getUsuarios().get(jcbPasajeros.getSelectedIndex());
        if (ValidadorSolicitarPasajero(temporal)) {
            listaPasajerosViaje.add(temporal);
            CargadorSolicitarPasajerosAgregados();
            JOptionPane.showMessageDialog(null, "El pasajero: " + temporal.getNombreCompleto()
                    + "\nCedula:" + temporal.getCedula()
                    + "\nse agrego Exitosamente a"
                    + "\nla lista de viaje", "Mensaje", 1);
        }

    }//GEN-LAST:event_btnSolicitarAgregarPasajeroActionPerformed

    private void btnSolicitarViajeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSolicitarViajeActionPerformed
       
        if(ValidarCamposSolicitudViaje()){
            SimpleDateFormat traductor = new SimpleDateFormat("dd-MM-yyyy");
        Date fechaActrual = new Date();
        Date fechatemp;
        try {
            Viaje nuevoViaje = new Viaje();
            fechaActrual = traductor.parse(traductor.format(fechaActrual));

            nuevoViaje.setPuntoSalida(jtfSolicitarPunto.getText());
            fechatemp = traductor.parse(jtfSolicitarFechaInicio.getText());
            nuevoViaje.setFechaInicio(fechatemp);
            fechatemp = traductor.parse(jtfSolicitarFechaFinalizacion.getText());
            nuevoViaje.setFechaFinalizacion(fechatemp);

            nuevoViaje.setFechaSolicitud(fechaActrual);

            nuevoViaje.setPasajeros(listaPasajerosViaje);

            nuevoViaje.setConsecutivo("IVA-" + implementacion.miControlador.getMiTec().getViajes().size());

            if (usuarioActual.SolicitarViaje(nuevoViaje)) {
                JOptionPane.showMessageDialog(null, "Viaje Agregado");
                LimpiarCamposSolicitarViaje();
            } else {
                JOptionPane.showMessageDialog(null, "La solicitud de viaje\n"
                        + "fue Negada",
                         "Alerta", 0);
            }

        } catch (Exception e) {
            System.out.println(e.toString());
            JOptionPane.showMessageDialog(null, "Algun dato fue Agregado\n"
                    + "sin el formato Correcto",
                     "Alerta", 2);
        }
        }
        

    }//GEN-LAST:event_btnSolicitarViajeActionPerformed

